#include "cub3d.h"

/**
 * check if a char is '\t', '\n', '\v', '\f', '\r' or ' '
*/
bool	ft_isspace(char c)
{
	if (c == ' ' || c == '\t' || c == '\n' || \
	c == '\v' || c == '\f' || c == '\r')
		return (true);
	return (false);
}
