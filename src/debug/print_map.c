#include "cub3d.h"

void	print_map(char **map)
{
	int	i;

	i = 0;
	ft_printf(1, "----- MAP -----\n");
	while (map[i])
	{
		ft_printf(1, "%s\n", map[i]);
		i++;
	}
	ft_printf(1, "\n");
}
